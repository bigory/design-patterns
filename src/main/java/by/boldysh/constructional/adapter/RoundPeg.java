package by.boldysh.constructional.adapter;

/**
 * Круглые Колышки совместимы с Круглыми Отверстиями.
 */
public class RoundPeg {
    private double radius;

    public RoundPeg() {}

    public RoundPeg(double radius) {
        this.radius = radius;
    }

    public double getRadius() {
        return radius;
    }
}
